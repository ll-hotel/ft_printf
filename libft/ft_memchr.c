#include <stddef.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*p;

	p = (unsigned char *)s;
	while (n-- && *p != (unsigned char)c)
		p++;
	if (n + 1)
		return (p);
	return (0);
}
